module.exports.run = function({ api, event, client, __GLOBAL }) {
    const { threadID, messageID, senderID, body } = event;

    // Check if the received message contains any of the predefined emoji
    for (const e of emoji) {
        if (body.includes(e)) {
            // Get a random reply for the matched emoji
            const randomReply = replies[e][Math.floor(Math.random() * replies[e].length)];

            // Mention the user who sent the message
            const mention = {
                tag: senderID,
                id: senderID
            };

            // Send the mentioned reply
            api.sendMessage({ body: `${mention.tag} ${randomReply}`, mentions: [mention] }, threadID, () => api.sendMessage(randomReply, threadID));
            break;
        }
    }
}

